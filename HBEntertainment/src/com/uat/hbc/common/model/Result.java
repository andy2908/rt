package com.uat.hbc.common.model;

public class Result {

	private String status;
	private String msg;
	private String genMstId;
	private String genUserId;
	private String opId;
	private String docNo;
	private String errorMsg;
	private String jsonValues;
	private String groupId;
	private String sessionId;
	
	

	public String getGroupId() {
		return groupId;
	}

	public void setGroupId(String groupId) {
		this.groupId = groupId;
	}

	public String getSessionId() {
		return sessionId;
	}

	public void setSessionId(String sessionId) {
		this.sessionId = sessionId;
	}

	public String getErrorMsg() {
		return errorMsg;
	}

	public void setErrorMsg(String errorMsg) {
		this.errorMsg = errorMsg;
	}

	public String getJsonValues() {
		return jsonValues;
	}

	public void setJsonValues(String jsonValues) {
		this.jsonValues = jsonValues;
	}

	public String getOpId() {
		return opId;
	}

	public void setOpId(String opId) {
		this.opId = opId;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	public String getGenMstId() {
		return genMstId;
	}

	public void setGenMstId(String genMstId) {
		this.genMstId = genMstId;
	}

	public String getDocNo() {
		return docNo;
	}

	public void setDocNo(String docNo) {
		this.docNo = docNo;
	}

	public String getGenUserId() {
		return genUserId;
	}

	public void setGenUserId(String genUserId) {
		this.genUserId = genUserId;
	}

	@Override
	public String toString() {
		return "Result [status=" + status + ", msg=" + msg + ", genMstId="
				+ genMstId + ", genUserId=" + genUserId + ", opId=" + opId
				+ ", docNo=" + docNo + ", errorMsg=" + errorMsg
				+ ", jsonValues=" + jsonValues + " , groupId=" + groupId + " , sessionId=" + sessionId + " ]";
	}
	


}
